var placebo = window.placebo;

// Create a <p> element
placebo('p');

// Create a <p> element and place it in the document
placebo('p').place();

// Create a <p> with id foo
placebo('p#foo');

// Create a <div> with id bar
placebo('#bar');

// Create a <p> with class baz
placebo('p.baz');

// Create a <div> with class baz
placebo('.baz');